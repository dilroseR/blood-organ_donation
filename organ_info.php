    <p class="lists"><h2><b>What is organ donation and transplantation?</b></h2></p> 
    <p class="lists">Organ donation is the process of surgically removing an organ or tissue from one person (the organ donor) and placing it into another person (the recipient). Transplantation is necessary because the recipient’s organ has failed or has been damaged by disease or injury.</p>
    <p class="lists"><h4><b>Who can be an organ donor?</p></h4></b>
    <p class="lists">People of all ages should consider themselves potential donors. When a person dies, he or she is evaluated for donor suitability based on their medical history and age.</p>
    
    <p class="lists"><h4><b>Benefits of organ donation: </b><br><h5><strong><u>For the recipient:-</h5></strong></p></h4></u>
    <ul class="lists">
      <li><strong>Quality of life:</strong> Transplants can greatly improve a recipient’s health and quality of life, allowing them to return to normal activities. They can spend more time with family and friends, be more physically active, and pursue their interests more fully.</li>
      <li><strong>Shorter waiting time: </strong> Due to the lack of organs available for transplant, patients on the national transplant list often face long wait times (sometimes several years) before they are able to receive a transplant from a deceased donor. Patients who find a suitable living donor do not have to wait on the list.</li>
    </ul>
    <p class="lists"><h5><strong><u>For the living donor:-</h5></p></strong></u>
      <li><strong>Positive emotional experiences:</strong>The gift of an organ can save the life of a transplant candidate. The experience of providing this special gift to a person in need can serve be a positive aspect of donation.</li>
      <li><strong>More time with your loved one:</strong>Donating an organ can increase the time you have to spend with your loved one as well as the quality of that time.</li>    
    </ul>

